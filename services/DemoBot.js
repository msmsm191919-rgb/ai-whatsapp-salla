// تطبيع النص العربي (تشكيل + توحيد الحروف)
function normalize(text = '') {
    return String(text)
        .toLowerCase()
        .replace(/[ًٌٍَُِّْ]/g, '')
        .replace(/[إأآا]/g, 'ا')
        .replace(/ى/g, 'ي')
        .replace(/ة/g, 'ه')
        .replace(/\s+/g, ' ')
        .trim();
}

