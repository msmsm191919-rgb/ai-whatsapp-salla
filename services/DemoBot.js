class DemoBot {
    reply(message) {
        const text = normalize(message);
        if (!text) {
            return { reply: INTENTS[0].resolve('').text, tag: 'greeting' };
        }

        // 1) منتج محدد بالاسم (إلا لو يسأل عن الكتالوج ككل)
        const asksCatalog = ['منتجات', 'المنتجات', 'الكتالوج', 'وش تبيعون', 'ايش تبيعون'].some(k => text.includes(normalize(k)));
        if (!asksCatalog) {
            for (const p of PRODUCTS) {
                if (p.key.some(k => text.includes(normalize(k)))) {
                    return {
                        reply: productCard(p) + '\n\nتحب أضيفه لسلتك وأجهّز لك الطلب؟ 🛒',
                        tag: 'product_detail',
                        badge: { icon: 'fa-bag-shopping', label: 'تفاصيل المنتج', color: 'emerald' }
                    };
                }
            }
        }

        // 2) مطابقة النية
        let best = null, bestScore = 0;
        for (const intent of INTENTS) {
            if (!intent.keywords || intent.keywords.length === 0) continue;
            let score = 0;
            for (const kw of intent.keywords) {
                if (text.includes(normalize(kw))) score++;
            }
            if (score > bestScore) { bestScore = score; best = intent; }
        }

        if (best && bestScore > 0) {
            if (typeof best.resolve === 'function') {
                const r = best.resolve(text);
                return { reply: r.text, tag: best.tag, badge: r.badge || best.badge || null };
            }
            const r = pick(best.replies);
            return { reply: r.text, tag: best.tag, badge: best.badge || null };
        }

        return { reply: pick(FALLBACKS).text, tag: 'fallback' };
    }

    suggestions() {
        return [
            'السلام عليكم 👋',
            'وش تقدمون؟ 🛍️',
            'أعرض المنتجات 📦',
            'كم سعر الشحن؟ 🚚',
            'في عروض أو خصومات؟ 🎁'
        ];
    }
}

